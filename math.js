/**
 * Function to get the sqrt root of a number
 * @param {Integer} a
 * @returns
 */
const sqrt = (a) => {
  return Math.sqrt(a);
};

/**
 * function to calculate perimeter
 * @param {Number} a
 * @param {Number} b
 * @returns
 */
const perimeter = (a, b) => {
  return 2 * a + 2 * b;
};

const substraction = (a, b) => {
  return a - b;
};
